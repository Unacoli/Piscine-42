int	ft_iterative_factorial(int nb)
{
	int i;
	int f;

	f = 1;
	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (i <= nb)
	{
		f = f * i;
		i++;
	}
	return (f);
}
